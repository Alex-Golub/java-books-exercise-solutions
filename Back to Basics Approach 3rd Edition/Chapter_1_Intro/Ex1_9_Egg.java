class Ex1_9_Egg {
  public static void main(String[] args) {
    upper();
    System.out.println("-\"-'-\"-'-\"-");
    lower();
  }

  public static void upper() {
    System.out.println("  _______ ");
    System.out.println(" /       \\");
    System.out.println("/         \\");
  }

  public static void lower() {
    System.out.println("\\         /");
    System.out.println(" \\_______/");
  }
}