package chapter4_conditionals;

/**
 * 13. Write a method called average that takes two integers as parameters
 * and returns the average of the two integers.
 *
 * @author Mr.Dr.Professor
 * @since 10-Dec-20 7:16 PM
 */
class Ex4_13_average {
  public static double average(int a, int b) {
    return (a + b) / 2.0;
  }
}
